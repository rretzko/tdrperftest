                    'school_id' => $seed[1],
                    'user_id' => $seed[2],
                    'created_at' => $now,
                    'updated_at' => $now,
                ]);
        }
    }

    private function seedPeopleTable($seed, $now)
    {
        $model = new Person;

        $model->user_id = $seed[0];
        $model->first = $seed[1];
        $model->middle = $seed[2];
        $model->last = $seed[3];
        $model->pronoun_id = $seed[4];
        $model->honorific_id = $seed[5];
        $model->created_at = $now;
        $model->updated_at = $now;

        $model->save();
    }

    private function seedRolesTable($seed, $now)
    {
        static $roletype_id;
        $model = new Role;

        if(! $roletype_id){
            $roletype_id = Roletype::where('descr', 'teacher')->first()->id;
        }

        $model->user_id = $seed[0];
        $model->roletype_id = $roletype_id;
        $model->created_at = $now;
        $model->updated_at = $now;

        $model->save();
    }

    private function seedSchoolUserTable()
    {
        foreach($this->schoolteacherseeds AS $seed){

            DB::table('school_user')
                ->insert([
                    'school_id' => $seed[0],
                    'user_id' => $seed[1],
                ]);
        }
    }

    private function seedTenuresTable()
    {
        foreach($this->tenureseeds AS $seed){

            $model = new Tenure;

            $model->school_id = $seed[0];
            $model->user_id = $seed[1];
            $model->startyear = $seed[2];
            $model->endyear = $seed[3];

            $model->save();
        }
    }

    private function seedTeachersTable($seed, $now)
    {
        $model = new Teacher;

        $model->user_id = $seed[0];
        $model->created_at = $now;
        $model->updated_at = $now;

        $model->save();
    }
