    {
        $now = Carbon::now();

        foreach($this->seeds AS $seed) {
            $this->seedPeopleTable($seed, $now);
            $this->seedRolesTable($seed, $now);
            $this->seedTeachersTable($seed, $now);
        }

        $this->seedSchoolUserTable();
        $this->seedTenuresTable();
        $this->seedGradetypeSchoolUserTable($now);
    }

