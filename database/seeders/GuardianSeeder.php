class GuardianSeeder extends Seeder
{
    private $now;
    private $peopleseeds;
    private $guardianstudentseeds;

    public function __construct()
    {
        $this->now = Carbon::now();

        //instantiate $this->*seeds with guardian data
        $this->peopleseeds = $this->buildPeopleSeeds();
        $this->guardianstudentseeds = $this->buildGuardianStudentSeeds();
    }/**
     * Run the database seeds.
     *
     * @return void
     */
    public function run()
    {
        $this->seedPeopleTable();
        $this->seedRolesDuplicatesTable();
        $this->seedGuardianStudentTable();
    }
/** END OF PUBLIC FUNCTIONS  *************************************************/

    private function seedGuardianStudentTable()
    {
        foreach($this->guardianstudentseeds AS $seed){

            DB::table('guardian_student')
                ->insert([
                    'guardian_user_id' => $seed[0],
                    'student_user_id' => $seed[1],
                    'guardiantype_id' => $seed[2],
                ]);
        }
    }

    private function seedPeopleTable()
    {
        foreach($this->peopleseeds AS $seed){

            $model = new Person();

            $model->user_id = $seed[0];
            $model->first = $seed[1];
            $model->middle = $seed[2];
            $model->last = $seed[3];
            $model->pronoun_id = $seed[4];
            $model->honorific_id = $seed[5];
            $model->created_at = $this->now;
            $model->updated_at = $this->now;

            $model->save();

            Guardian::create(['user_id' => $seed[0]]);

            $this->seedRolesTable($seed);
        }
    }

    private function seedRolesTable($seed)
    {
        static $roletype_id;
        $model = new Role;

        if(! $roletype_id){
            $roletype_id = Roletype::where('descr', 'guardian')->first()->id;
        }

        $model->user_id = is_array($seed) ? $seed[0] : $seed;
        $model->roletype_id = $roletype_id;
        $model->created_at = $this->now;
        $model->updated_at = $this->now;

        $model->save();
    }

    /**
     * Some guardians are also Teacher or Guardians for other students
     * These cannot be role'd through the seedPeopleTable() and must
     * be managed separately
     */
    private function seedRolesDuplicatesTable()
    {
        $duplicates = [647,1161,1403,1621,3592,6986,7056,7059,7232,7355,7358,7364,7380,7415,7420,7423,7438,7443,7449,
            7452,7493,7503,7697,7935,];

        foreach($duplicates AS $user_id){

            $this->seedRolesTable($user_id);
        }
    }

